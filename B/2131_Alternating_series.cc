void solve(){
    int n;
    cin >> n;

    if (n == 2) {
        cout << -1 << " " << 2 << endl;
        return;
    }

     vector<int> a(n, -1);
    for (int i = 1; i < n; i += 2) {
        if (i == n - 1) {
            a[i] = 2;
        } else {
            a[i] = 3;
        }
    }

    for (int i = 0; i < n; ++i) {
        cout << a[i] << (i == n - 1 ? "" : " ");
    }
    cout << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while(t--){
        solve();
    }

    return 0;
}